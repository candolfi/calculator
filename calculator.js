const add = function(num1, num2){
    const sum = num1 + num2;
    return sum;
}

const subtract = function(num1, num2){
    const difference = num1 - num2;
    return difference;
}

const multiply = function(num1, num2){
    const product = num1 * num2;
    return product;
}

const divide = function(num1, num2){
    const quotient = num1 / num2;
    return quotient;
}

